public class Matakuliah {
    public String kode;
    public String nama;
    public int sks; 
    public int jumlahJam;

    public Matakuliah(String kode, String nama, int sks, int jumlahJam) {
        this.kode = kode;
        this.nama = nama;
        this.sks = sks;
        this.jumlahJam = jumlahJam;
    }
}